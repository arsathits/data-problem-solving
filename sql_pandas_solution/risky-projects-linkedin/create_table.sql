-- 1️⃣ Create Schema
CREATE SCHEMA "risky_projects_linkedin";

SET search_path TO "risky_projects_linkedin";

-- 2️⃣ Create Tables
CREATE TABLE linkedin_projects (
    id INT PRIMARY KEY,
    title VARCHAR(100),
    budget NUMERIC,
    start_date DATE,
    end_date DATE
);

CREATE TABLE linkedin_employees (
    id INT PRIMARY KEY,
    first_name VARCHAR(100),
    last_name VARCHAR(100),
    salary NUMERIC
);

CREATE TABLE linkedin_emp_projects (
    emp_id INT REFERENCES linkedin_employees(id),
    project_id INT REFERENCES linkedin_projects(id)
);

